function find_replace($keyword,$text)
{
	@$pos=strpos($text,$keyword);
	if ($pos !== false) {
	     return 1;
	} else {
	     return 0;
	}
}
function do_replace($imgword,$keyword,$text)
{
	$text=str_replace($keyword, $imgword, $text);
	return $text;
}
